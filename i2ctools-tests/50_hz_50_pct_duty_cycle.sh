#!/bin/bash

I2C_BUS=${I2C_BUS:-1}

# Sleep / Allcall enabled
i2ctransfer -y ${I2C_BUS} w2@0x40 0x00 0x11

# 50 Hertz
i2ctransfer -y ${I2C_BUS} w2@0x40 0xFE 0x84

# Start 0 - end 0x800 = 50% duty cycle
i2ctransfer -y ${I2C_BUS} w2@0x40 0x06 0x00 w2@0x40 0x07 0x00 w2@0x40 0x08 0x00 w2@0x40 0x09 0x08

# Take out of sleep
i2ctransfer -y ${I2C_BUS} w2@0x40 0x00 0x01
